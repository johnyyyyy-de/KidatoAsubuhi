#Arithmetic Operators
num1 = 10
num2 = 5

print(num1 + num2)
print(num1 - num2)
print(num1 * num2)
print(num1 / num2)
print(num1 % num2)#modulus-returns the reminder after divsion

#Comparison Oparators
print(num1 < num2)
print(num1 > num2)
print(num1 <= num2)
print(num1 >=num2)
print(num1 == num2) #Equal to
print(num1 != num2) #Not Equal to

#Assignment Operators
x = 20


print(x)

x *= 2

print(x)

#logical Operators -and, or, not
print(30 < 45 and 100 > 250)
print(30 < 45  or 100 > 250)
print(not(30 < 45  or 100 > 250))
