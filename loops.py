#While loop

#Increament
number = 105
while number <= 110 :
    print(number)
    number += 1

#decreament
num = 30
while num >= 25 :
    print(num)
    num -= 1

#For loop
for count in range(1,10):
    print(count)

for x in range(20,30, 3):
    print("value is x",x)

language = ["Python","C++","Java"]
for lang in language :
    print(lang)